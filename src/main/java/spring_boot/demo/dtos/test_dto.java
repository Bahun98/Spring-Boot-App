package spring_boot.demo.dtos;

public class test_dto {
    private long id;

    private String name;

    public test_dto() {}

    public test_dto(long id, String name) {
        this.id = id;
        this.name = name;
    }

    public long getId() {
        return id;
    }

    public void setId(long id) {
        this.id = id;
    }

    public String getName() {
        return name;
    }

    public void setName(String name) {
        this.name = name;
    }
}
